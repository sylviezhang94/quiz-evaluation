"""LLM 评判评分卡

八维度评分标准、问题标签体系、严重程度分级 —— 全部来自飞书文档「题目测评标准-教研侧」原文。
"""

from models import DIMENSIONS, PROBLEM_TAGS


# ════════════════════════════════════════════
# 八维度 0/1/2 评分标准（原文逐字）
# ════════════════════════════════════════════
SCORING_RUBRIC = """
## 八维度绝对分标准

| 维度 | 0分（有瑕疵） | 1分（可用，有待优化） | 2分（满意） |
|-|-|-|-|
| **知识点匹配** | 题目与知识点相关，但考察方向有偏移或不够聚焦 | 题目精准围绕知识点，但少数情况下表述可更清晰 | 题目精准围绕知识点，考察点清晰 |
| **题型合规性** | 题型完全符合规范，但UI/交互细节可优化（如应4选项但只有3个有效选项） | 题型完全符合规范，但UI/交互细节可优化 | 题型完全符合该级别的规范要求 |
| **语言正确性** | 无语法拼写错误，但表述略显生硬或不够自然 | 语言基本准确自然，有个别不太地道的表达但不影响理解 | 语言准确、自然、地道 |
| **级别适配度** | 有1-2个超纲词或句长略超出范围，但不影响核心理解 | 词汇和句长基本符合级别特征，偶有轻微偏差 | 词汇、句长、语法完全符合级别特征 |
| **选项设计** | 干扰项有一定迷惑性但偏弱，或答案唯一性有轻微歧义 | 干扰项设计合理，答案唯一，但区分度可进一步提升 | 答案唯一，干扰项设计巧妙，能有效测试 |
| **题干清晰度** | 用户能理解题目要求但需思考一下，或题干有轻微冗余信息 | 题目基本清晰易懂，但措辞可进一步优化 | 题目清晰易懂，无歧义 |
| **与剧情/语境关联** | 缺少必要的上下文支撑答题，用户无法理解 | 有一定上下文但不够充分，需要用户猜测语境 | 语境充分，能有效支撑答题 |
| **交互体验** | 题目出现时机影响观看节奏，或反馈完全无效 | 出题时机稍有打断感，或反馈解释不够清晰 | 题目出现时机自然，反馈有效，体验流畅 |
"""


# ════════════════════════════════════════════
# 严重程度分级（原文逐字）
# ════════════════════════════════════════════
SEVERITY_TIERS = """
## 严重程度分级

| 严重程度 | 定义 | 示例 |
|-|-|-|
| **高（High）** | 导致题目不可用，必须修改 | 答案错误、知识点错配、语法错误影响理解 |
| **中（Medium）** | 影响题目质量，但可优化后使用 | 干扰项与正确答案语义相近（可能被误选）、题干有歧义但不影响大部分用户理解、超纲词1-2个 |
| **低（Low）** | 可改可不改，不影响核心功能 | 表达不够地道、句长略超 |
"""


# ════════════════════════════════════════════
# 问题标签体系（原文逐字）
# ════════════════════════════════════════════
def build_tag_catalog() -> str:
    lines = ["## 标签分类总览\n"]
    lines.append("| 大类 | 标签 | 定义 |")
    lines.append("|-|-|-|")
    for t in PROBLEM_TAGS:
        lines.append(f"| {t.category} | {t.name} | {t.definition} |")
    return "\n".join(lines)


# ════════════════════════════════════════════
# 单题评判输出格式
# ════════════════════════════════════════════
EVALUATION_OUTPUT_FORMAT = """
## 输出格式要求

对每道题，输出一个 JSON 对象，格式如下：

```json
{
  "question_id": "Q001",
  "dimension_scores": [
    {"dimension": "知识点匹配", "score": 2, "reason": "题目精准围绕知识点xxx，考察点清晰"},
    {"dimension": "题型合规性", "score": 2, "reason": "完全符合A2级REVIEW_S1的规范要求"},
    {"dimension": "语言正确性", "score": 1, "reason": "选项'apex'略显生僻，但不影响核心理解"},
    {"dimension": "级别适配度", "score": 2, "reason": "词汇apple为A1词，选项均在A2范围内"},
    {"dimension": "选项设计", "score": 1, "reason": "干扰项'apply'为有效干扰，但'ape'和'apex'读音相近但区分度一般"},
    {"dimension": "题干清晰度", "score": 2, "reason": "题干清晰，指令明确"},
    {"dimension": "与剧情/语境关联", "score": 2, "reason": "有源台词上下文支撑"},
    {"dimension": "交互体验", "score": 1, "reason": "听音选义的播放次数未明确标注，建议补充"}
  ],
  "tags": [{"code": "O-3", "severity": "Medium"}],
  "suggestions": "建议将干扰项'apex'替换为更常见的近似读音词如'april'"
}
```

**注意**：
- score 必须是 0、1 或 2 的整数
- dimension 名称必须与八维度名称完全一致
- tags 是问题标签数组，每个元素含 code（标签代码）和 severity（High/Medium/Low），无问题时为空数组 []
- 严重程度判定参考上文「严重程度分级」：High=导致题目不可用必须修改，Medium=影响质量但可优化，Low=可改可不改
- reason 和 suggestions 用中文撰写，简洁有力
- 请认真分析题目内容，不要敷衍地给所有维度打满分或统一分数
- 评判时严格对照上文「八维度绝对分标准」中的分数定义
"""


# ════════════════════════════════════════════
# 系统 prompt 模板
# ════════════════════════════════════════════
def build_evaluation_prompt(rule_text: str) -> str:
    """构建单题评判的完整 prompt"""
    return f"""你是一个英语教学题目质量评测专家。请按照以下标准评测一道题目。

{rule_text}

{SCORING_RUBRIC}

{build_tag_catalog()}

{SEVERITY_TIERS}

{EVALUATION_OUTPUT_FORMAT}

请开始评测题目。
"""
